def insert(link, value, index):
    """Insert a value into a Link at the given index.

    >>> link = Link(1, Link(2, Link(3)))
    >>> print_link(link)
    <1 2 3>
    >>> insert(link, 9001, 0)
    >>> print_link(link)
    <9001 1 2 3>
    >>> insert(link, 100, 2)
    >>> print_link(link)
    <9001 1 100 2 3>
    >>> insert(link, 4, 5)
    IndexError
    """
    "*** YOUR CODE HERE ***"
    if index >= link.__len__():
        raise IndexError
    else:
        if index == 0:
            x = link.first
            link.first = value
            link.rest = Link(x, link.rest)
        else:
            insert(link.rest, value, index - 1)
