def reverse(link):
    """Returns a Link that is the reverse of the original.

    >>> print_link(reverse(Link(1)))
    <1>
    >>> link = Link(1, Link(2, Link(3)))
    >>> new = reverse(link)
    >>> print_link(new)
    <3 2 1>
    >>> print_link(link)
    <1 2 3>
    """
    "*** YOUR CODE HERE ***"
    final_lst = Link(link.first)
    rest = link.rest
    while rest is not Link.empty:
        x = Link(rest.first, final_lst)
        final_lst = x
        rest = rest.rest
    return final_lst


